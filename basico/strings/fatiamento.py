nome = 'Matheus Henrique Vieira Ramos Nobre'

print(nome[0]) # Acessando um caractere específico
print(nome[:7]) # Acessando do 1º caractere até o caractere desejado
print(nome[17:]) # Acessando do caractere desejado até o último
print(nome[8:16]) # Acessando do caractere desejado de início até o caractere desejado de final
print(nome[8:16:2]) # Acessando os caracteres no intervalo desejado, de forma que se pule de 2 em 2
print(nome[:]) # Acessando toda a string
print(nome[::-1]) # Acessando a string de trás para frente (fazendo seu espelhamento)

# Importante lembrar que isso também vale para vetores e tuplas